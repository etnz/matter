func TestProtocolHeaderDecoding(t *testing.T) {
	for _, tc := range testData {
		t.Run(tc.name, func(t *testing.T) {
			// Construct a header
			h := protocolMessageHeader{
				ExchangeFlags: 0, // Default
				Opcode:        tc.opcode,
				ExchangeID:    1234,
				ProtocolId:    tc.protocolId,
			}

			var buf bytes.Buffer
			h.Encode(&buf)
			buf.Write(tc.payload)

			// Decode
			decodedH, decodedPayload, err := decodeProtocolMessageHeader(buf.Bytes())
			if err != nil {
				t.Fatalf("Failed to decode header: %v", err)
			}

			if decodedH.Opcode != tc.opcode {
				t.Errorf("Expected opcode %v, got %v", tc.opcode, decodedH.Opcode)
			}
			if decodedH.ProtocolId != tc.protocolId {
				t.Errorf("Expected protocolId %v, got %v", tc.protocolId, decodedH.ProtocolId)
			}
			if !bytes.Equal(decodedPayload, tc.payload) {
				t.Errorf("Payload mismatch")
			}
		})
	}
}

func TestMessageHeader_EncodeDecode(t *testing.T) {
	// Helper to create 8-byte NodeID
	nodeID := func(id uint64) []byte {
		b := make([]byte, 8)
		binary.LittleEndian.PutUint64(b, id)
		return b
	}
	// Helper to create 2-byte GroupID/NodeID
	groupID := func(id uint16) []byte {
		b := make([]byte, 2)
		binary.LittleEndian.PutUint16(b, id)
		return b
	}

	tests := []struct {
		name     string
		header   messageHeader
		expected messageHeader // Expected after decode (flags might change)
	}{
		{
			name:   "MessageCounter only",
			header: messageHeader{MessageCounter: 123},
			expected: messageHeader{
				MessageCounter: 123,
				Flags:          0,
			},
		},
		{
			name: "Source and Dest NodeID (64-bit)",
			header: messageHeader{
				MessageCounter:    222,
				SourceNodeID:      nodeID(1),
				DestinationNodeID: nodeID(2),
			},
			expected: messageHeader{
				MessageCounter:    222,
				SourceNodeID:      nodeID(1),
				DestinationNodeID: nodeID(2),
				Flags:             0x04 | 0x01, // Source present (bit 2) | Dest 64-bit (bit 0)
			},
		},
		{
			name: "Source NodeID only",
			header: messageHeader{
				MessageCounter: 123,
				SourceNodeID:   nodeID(55),
			},
			expected: messageHeader{
				MessageCounter: 123,
				SourceNodeID:   nodeID(55),
				Flags:          0x04,
			},
		},
		{
			name: "Dest NodeID only",
			header: messageHeader{
				MessageCounter:    123,
				DestinationNodeID: nodeID(11),
			},
			expected: messageHeader{
				MessageCounter:    123,
				DestinationNodeID: nodeID(11),
				Flags:             0x01,
			},
		},
		{
			name: "Source and Dest NodeID (64-bit) different values",
			header: messageHeader{
				MessageCounter:    234,
				SourceNodeID:      nodeID(77),
				DestinationNodeID: nodeID(88),
			},
			expected: messageHeader{
				MessageCounter:    234,
				SourceNodeID:      nodeID(77),
				DestinationNodeID: nodeID(88),
				Flags:             0x05,
			},
		},
		{
			name: "Secure Session Control Msg",
			header: messageHeader{
				MessageCounter:    234,
				SourceNodeID:      nodeID(77),
				DestinationNodeID: nodeID(88),
				SecurityFlags:     0x20, // Control Msg bit? Just testing persistence
			},
			expected: messageHeader{
				MessageCounter:    234,
				SourceNodeID:      nodeID(77),
				DestinationNodeID: nodeID(88),
				SecurityFlags:     0x20,
				Flags:             0x05,
			},
		},
		{
			name: "Session ID",
			header: messageHeader{
				MessageCounter:    234,
				SourceNodeID:      nodeID(77),
				DestinationNodeID: nodeID(88),
				SessionID:         2,
			},
			expected: messageHeader{
				MessageCounter:    234,
				SourceNodeID:      nodeID(77),
				DestinationNodeID: nodeID(88),
				SessionID:         2,
				Flags:             0x05,
			},
		},
		{
			name: "Dest Group ID (16-bit)",
			header: messageHeader{
				MessageCounter:    234,
				SourceNodeID:      nodeID(77),
				DestinationNodeID: groupID(45),
			},
			expected: messageHeader{
				MessageCounter:    234,
				SourceNodeID:      nodeID(77),
				DestinationNodeID: groupID(45),
				Flags:             0x04 | 0x02, // Source present | Dest 16-bit
			},
		},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			var buf bytes.Buffer
			tc.header.Encode(&buf)

			var decoded messageHeader
			if err := decoded.Decode(&buf); err != nil {
				t.Fatalf("Decode failed: %v", err)
			}

			// Verify fields
			if decoded.MessageCounter != tc.expected.MessageCounter {
				t.Errorf("MessageCounter: got %v, want %v", decoded.MessageCounter, tc.expected.MessageCounter)
			}
			if decoded.SessionID != tc.expected.SessionID {
				t.Errorf("SessionID: got %v, want %v", decoded.SessionID, tc.expected.SessionID)
			}
			if decoded.SecurityFlags != tc.expected.SecurityFlags {
				t.Errorf("SecurityFlags: got %v, want %v", decoded.SecurityFlags, tc.expected.SecurityFlags)
			}
			if !bytes.Equal(decoded.SourceNodeID, tc.expected.SourceNodeID) {
				t.Errorf("SourceNodeID: got %x, want %x", decoded.SourceNodeID, tc.expected.SourceNodeID)
			}
			if !bytes.Equal(decoded.DestinationNodeID, tc.expected.DestinationNodeID) {
				t.Errorf("DestinationNodeID: got %x, want %x", decoded.DestinationNodeID, tc.expected.DestinationNodeID)
			}
			// Flags are calculated during Encode, so we check if they match expected
			if decoded.Flags != tc.expected.Flags {
				t.Errorf("Flags: got %x, want %x", decoded.Flags, tc.expected.Flags)
			}
		})
	}
}

